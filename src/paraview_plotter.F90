module paraview_plot
  use sizes, only : sz
  implicit none
  !.....Function API
  private
  public :: init_paraview_plotter(p)
  public :: paraview_plot_wrapper( )


  !.....Declare local variables
  real(sz), allocatable :: XPNP(:), YPNP(:)
  integer, allocatable :: triangulation(:,:)
  integer :: plt_iter = 0

contains

  SUBROUTINE INIT_PARAVIEW_PLOTTER(p)
    implicit none
    integer, intent(in) :: p
    !......Obtain the nodal points for plotting
    GET_INTERP_PTS(XPNP, YPNP, triangulation, p)

    !......Compute modal values at (XPNP,YPNP)
    ORTHOBASIS_PLOT(XPNP,YPNP,PHI_PLOT,p)

    RETURN
  END SUBROUTINE INIT_PARAVIEW_PLOTTER

  SUBROUTINE PARAVIEW_PLOT(ZE,B,QX,QY,plot_var)
    use plot_var_funcs
    use dg, only : 
    use global, only : NE,X,Y
    implicit none

    abstract interface
       pure function plot_var(ze,b,qx,qy)
         real(sz) :: plot_var
         real(sz), intent(in) :: ze,b,qx,qy
       end function plot_var
    end interface

    !.....declare arguments
    real(sz), allocatable, intent(in) :: ZE(:,:), QX(:,:), QY(:,:)
    real(sz), allocatable, intent(in) :: b(:,:)
    integer, intent(in) :: iter

    !.....declare internal variables
    procedure (func), pointer :: f_ptr => null ()
    integer j,k,i,l
    integer n_interp_pts, n_interp_tri
    real(sz) ell_1,ell_2, ell_3
    real(sz) ze_val, b_val, qx_val, qy_val
    character*25 :: fname = "dgswem____00000.00000.vtu"

    plt_iter = plt_iter + 1
    fname(:9) = plot_var
    fname(:15) = plt_iter
    fname(:21) = MYPROC
    open(877,file= fname, action="write")


    n_interp_pts = size(xpnp)
    n_interp_tri = size(triangulation,1)

    SELECT CASE (plot_var)
    CASE ("ze")
       f_ptr => ze
    CASE ("h")
       f_ptr => h
    CASE ("b")
       f_ptr => b
    CASE ("qx")
       f_ptr => qx_o
    CASE ("qy")
       f_ptr => qy_o
    CASE ("u")
       f_ptr => u_o
    CASE ("v")
       f_ptr => v_o
    CASE DEFAULT
       print *, "ERROR:: BAD plot_var in paraview_plotter.F90"
    END SELECT

    write(877,*) "<?xml version=\"1.0\" ?>"
    write(877,*) "<!--"
    write(877,*) "# Generated by ArcSyn3sis"
    write(877,*) "-->"
    write(877,*) "<VTKFile type=\"UnstructuredGrid\" version=\"0.1\" &
         &byte_order=\"BigEndian\">"
    !     write(877,*) " compressor=\"vtkZLibDataCompressor\"";
    !     write(877,*) " byte_order=\"LittleEndian\"";

    write(877,*) "  <UnstructuredGrid>"
    output << "    <Piece NumberOfPoints=\"" << n_interp_pts*NE
    <<"\" NumberOfCells=\"" << n_interp_tri*NE << "\">"
    write(877,*) "      <Points>"
    write(877,*) "        <DataArray type=\"Float64\"&
         & NumberOfComponents=\"3\" Format=\" ascii \">" !As in 3D, x y z

    DO J = 1,NE
       !obtain global coordinates
       n1 = nm(j,1)
       n2 = nm(j,2)
       n3 = nm(j,3)

       DO i = 1,NPNP
          ell_1 = -0.5D0 * ( XPNP(i) + ypnp(i))
          ell_2 =  0.5D0 * ( XPNP(i) + 1.D0 )
          ell_3 =  0.5D0 * ( YPNP(i) + 1.D0 )
       ENDDO

       xg = x(n1)*ell_1 + x(n2)*ell_2 + x(n3)*ell_3
       yg = y(n1)*ell_1 + y(n2)*ell_2 + y(n3)*ell_3

       write(887,88702) xg,yg,"0.0"

    ENDDO
88702 FORMAT (fmt = "(8X,1E23.16,9X,1E23.16,9X,1E23.16,8X,(A),1X")


    write(877,*) "        </DataArray>"
    write(877,*) "      </Points>"
    write(877,*) "      <Cells>"
    write(877,*) "        <DataArray type=\"Float64\" Name=\"connectivity\"&
         &Format=\"ascii\">"

    DO J = 1,NE
       DO k=1,n_interp_tri
          write(877,88701) (triangulation(k,i)-1 + K_interp*(J-1), i = 1,3)
       ENDDO
    ENDDO
88701 FORMAT (8X,D7,1X)

    write(877,*) "        </DataArray>"
    write(877,*) "        <DataArray type=\"Int64\" Name=\"offsets\" Format=\"&
         &ascii\">"

    DO j = 1,NE
       DO k=1,K_interp
          write(887,fmt = "(7D,1X)",advance="no") 3*(k) + K_interp*(j-1)-1
       ENDDO
    ENDDO
    write(887,fmt="(a)") " " !newline

    write(877,*) "        </DataArray>"
    write(877,*) "        <DataArray type=\"Int64\" Name=\"types\" Format=\"&
         &ascii\">"
    DO j = 1,NE
       DO k=1,n_interp_p
          write(887,fmt = "(1D,1X)",advance="no") 5
       ENDDO
    ENDDO
    write(887,fmt="(a)") " "  !newline

    write(877,*) "        </DataArray>"
    write(877,*) "      </Cells>"
    write(877,*) "      <PointData Scalars=\"scalars\">"
    write(877,*) "        <DataArray type=\"Float64\" Name=\"Pressure\" Format=\"&
         &ascii\">"

    !.....For fully discontinuous output

    DO j = 1,NE
       DO i=1,n_interp_pts
          ze_val = 0
          b_val  = 0
          qx_val = 0
          qy_val = 0

          DO l = 1,size(phi_plot,1)
             ze_val = ze_val + ze(l,j)*phi_plot(l,i)
             b_val  = b_val  + b(l,j)*phi_plot(l,i)
             qx_val = qx_val + qx(l,j)*phi_plot(l,i)
             qy_val = qy_val + qy(l,j)*phi_plot(l,i)
          ENDDO

          write(887,fmt = "A",advance = "no") "        "
          write(887,*) f_ptr(ze_val,b_val,qx_val,qy_val)," "

       ENDDO
    ENDDO

    write(877,*) "        </DataArray>"
    write(877,*) "      </PointData>"
    write(877,*) "    </Piece>"
    write(877,*) "  </UnstructuredGrid>"
    write(877,*) "</VTKFile>"

    CLOSE(877)

    RETURN
  END SUBROUTINE PARAVIEW_PLOT



